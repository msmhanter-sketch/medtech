"""
app/normalizer/text_utils.py — Утилиты предобработки медицинских текстов.

Перед fuzzy-матчингом нужно привести оба текста к единому виду:
  - убрать лишние пробелы, скобки, спецсимволы
  - привести к нижнему регистру
  - расшифровать аббревиатуры (ОАК → общий анализ крови)
  - убрать «шумовые» слова (первичный, повторный, консультация...)
"""
import re
from typing import Optional

# ─── Словарь аббревиатур (расширяем по мере работы) ──────────────────────────
ABBREVIATIONS: dict[str, str] = {
    "oak":  "общий анализ крови",
    "оак":  "общий анализ крови",
    "оам":  "общий анализ мочи",
    "бак":  "биохимический анализ крови",
    "бх":   "биохимический анализ крови",
    "ттг":  "тиреотропный гормон",
    "tsh":  "тиреотропный гормон",
    "мно":  "международное нормализованное отношение",
    "ачтв": "активированное частичное тромбопластиновое время",
    "пв":   "протромбиновое время",
    "гм":   "головной мозг",
    "щж":   "щитовидная железа",
    "обп":  "органы брюшной полости",
    "кт":   "компьютерная томография",
    "мрт":  "магнитно-резонансная томография",
    "узи":  "ультразвуковое исследование",
    "экг":  "электрокардиограмма",
    "эхо":  "эхокардиография",
    "cbc":  "общий анализ крови",
    "mri":  "магнитно-резонансная томография",
    "uam":  "общий анализ мочи",
    "ls":   "пояснично-крестцовый",
}

# Слова-шум, которые не несут смысла для матчинга
NOISE_WORDS: frozenset[str] = frozenset({
    "первичный", "повторный", "первичная", "повторная",
    "консультация", "прием", "приём", "врача", "врач",
    "исследование", "анализ", "обследование",
    "без", "с", "по", "на", "в", "и", "или", "для",
    "стандартный", "расширенный", "базовый", "комплексный",
    "срочный", "обычный", "экспресс",
    "пункт", "единица", "шт", "мл", "мин",
})


def normalize_text(raw: str) -> str:
    """
    Нормализует произвольную строку названия медицинской услуги.

    Шаги:
      1. Unicode NFC-нормализация
      2. Нижний регистр
      3. Удаление HTML-тегов (на случай парсинга сайтов)
      4. Замена спецсимволов на пробел
      5. Расшифровка аббревиатур
      6. Удаление слов-шумов
      7. Коллапс пробелов

    Args:
        raw: Исходное название из прайса клиники.

    Returns:
        Нормализованная строка для fuzzy-сравнения.

    Example:
        >>> normalize_text("ОАК (первичный, СРОЧНО!)")
        'общий анализ крови'
    """
    import unicodedata

    if not raw or not raw.strip():
        return ""

    # 1. Нормализуем Unicode (NFС) — убирает проблемы с ё/е и похожими
    text = unicodedata.normalize("NFC", raw)

    # 2. Нижний регистр
    text = text.lower()

    # 3. Убираем HTML-теги
    text = re.sub(r"<[^>]+>", " ", text)

    # 4. Заменяем спецсимволы (кроме букв, цифр, дефиса) на пробел
    text = re.sub(r"[^а-яёәіңғүұқөһa-z0-9\s\-]", " ", text)

    # 5. Расшифровка аббревиатур — ищем изолированные токены
    tokens = text.split()
    expanded: list[str] = []
    for token in tokens:
        # Убираем дефисы по краям токена
        clean_token = token.strip("-")
        if clean_token in ABBREVIATIONS:
            # Раскрываем аббревиатуру
            expanded.extend(ABBREVIATIONS[clean_token].split())
        else:
            expanded.append(token)

    # 6. Удаляем шумовые слова
    filtered = [w for w in expanded if w not in NOISE_WORDS and len(w) > 1]

    # 7. Коллапс пробелов
    result = " ".join(filtered).strip()

    return result


def build_search_corpus(name: str, aliases_json: Optional[str]) -> list[str]:
    """
    Строит список вариантов для поиска из основного имени и JSON-алиасов.

    Args:
        name: Эталонное название услуги из БД.
        aliases_json: JSON-строка с массивом синонимов, например:
                      '["ОАК", "Клинический анализ крови", "CBC"]'

    Returns:
        Список нормализованных строк — все варианты для матчинга.
    """
    import json

    variants: list[str] = [normalize_text(name)]

    if aliases_json:
        try:
            aliases = json.loads(aliases_json)
            if isinstance(aliases, list):
                for alias in aliases:
                    if isinstance(alias, str) and alias.strip():
                        variants.append(normalize_text(alias))
        except (json.JSONDecodeError, ValueError):
            pass  # Игнорируем битый JSON, работаем только с name

    # Убираем дубли, сохраняем порядок (name — первый)
    seen: set[str] = set()
    unique: list[str] = []
    for v in variants:
        if v and v not in seen:
            seen.add(v)
            unique.append(v)

    return unique
