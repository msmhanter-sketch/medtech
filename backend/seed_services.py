# ──────────────────────────────────────────────────────────────────────────────

async def main():
    print("=== Seeding service catalog ===")

    async with AsyncSessionLocal() as db:
        # Ensure categories
        cat_map: dict[str, int] = {}
        for slug, name in CATEGORIES.items():
            existing = await db.scalar(select(ServiceCategory).where(ServiceCategory.slug == slug))
            if not existing:
                # Check by name too
                existing = await db.scalar(select(ServiceCategory).where(ServiceCategory.name == name))
            if not existing:
                cat = ServiceCategory(name=name, slug=slug, sort_order=list(CATEGORIES.keys()).index(slug))
                db.add(cat)
                await db.flush()
                cat_map[slug] = cat.id
                print(f"  + Category: {name}")
            else:
                # Update slug if missing
                if not existing.slug:
                    existing.slug = slug
                cat_map[slug] = existing.id

        await db.commit()

        # Upsert services
        added = 0
        updated = 0
        for canonical, aliases, cat_slug in SERVICES:
            cat_id = cat_map[cat_slug]
            existing = await db.scalar(
                select(Service).where(Service.name == canonical)
            )
            aliases_json = json.dumps(aliases, ensure_ascii=False)
            if not existing:
                svc = Service(
                    name=canonical,
                    category_id=cat_id,
                    aliases=aliases_json,
                    is_active=True,
                )
                db.add(svc)
                added += 1
            else:
                # Merge aliases
                current_aliases = json.loads(existing.aliases) if existing.aliases else []
                merged = list(set(current_aliases + aliases))
                existing.aliases = json.dumps(merged, ensure_ascii=False)
                existing.category_id = cat_id
                updated += 1

        await db.commit()

        total = await db.scalar(select(func.count()).select_from(Service))
        print(f"\n✅ Done! Added: {added}, Updated aliases: {updated}")
        print(f"   Total services in DB: {total}")

asyncio.run(main())
